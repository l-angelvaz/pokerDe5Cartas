    public void actualizar(Object evento, Observable origen) {
        if (evento.equals(EventosGenerales.Eventos.cambioListaTodasMesas)) {
            mostrarMesas();
            mostrarTotalRecaudado();
        }
    }

    private void mostrarMesas() {
        String[] columnas = {
            "NroMesa", "($)Apuesta Base", "Jug.Requeridos", "Jug.Actuales",
            "NroMano Actual", "($)Apostado", "(%)ComisiÃ³n", "($)Recaudado", "Estado"
        };
        Object[][] datos = Fachada.getInstancia().listarTodasLasMesas();
        DefaultTableModel modeloTabla = new DefaultTableModel(datos, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tablaMesas.setModel(modeloTabla);
        tablaMesas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tablaMesas.getTableHeader().setReorderingAllowed(false);
        tablaMesas.getTableHeader().setResizingAllowed(false);
        tablaMesas.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
    }

    private void obtenerYmostrarDetallesMesaSeleccionada() {
        int posicion = tablaMesas.getSelectedRow();
        if (posicion != -1) {
            int nroMesa = (int) tablaMesas.getValueAt(posicion, 0);
            ArrayList<String> detalles = Fachada.getInstancia().mostrarDetallesMesa(nroMesa);
            detallesMesaSeleccionada.setListData(detalles.toArray());
        }
    }

    private void crearMesa() throws MesaException, UsuarioException {
        try {
            String cantidadJugadoresText = tfCantidadJugadores.getText();
            String apuestaBaseText = tfApuestaBase.getText();
            String porcentajeText = tfPorcentaje.getText();

            if (cantidadJugadoresText.isEmpty() || apuestaBaseText.isEmpty() || porcentajeText.isEmpty()) {
                mostrarMensaje("Por favor, complete todos los campos.");
                return;
            }
            int cantJugadores = Integer.parseInt(cantidadJugadoresText);
            int apuestaBase = Integer.parseInt(apuestaBaseText);
            float comision = Float.parseFloat(porcentajeText);

            Fachada.getInstancia().crearMesa(apuestaBase, comision, cantJugadores);
        } catch (MesaException e) {
            mostrarMensaje(e.getMessage());
        }
    }

    public void mostrarTotalRecaudado() {
        int totalRecaudadoEnTodasLasMesas = Fachada.getInstancia().getTotalRecaudadoEnTodasLasMesas();
        String recaudado = "Total recaudado $" + totalRecaudadoEnTodasLasMesas;
        totalRecaudado.setText(recaudado);
    }

    public void mostrarMensaje(String mensaje) {
        campoMensaje.setText(mensaje);
    }

